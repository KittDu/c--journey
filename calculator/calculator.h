#ifndef _OOP_CALCULATOR_H_
#define _OOP_CALCULATOR_H_

#include <iostream>

// + function
double add (double a, double b)
{
    double c;
    return c = a + b;
}

// - function
double sub (double a, double b)
{
    double c;
    return c = a - b;
}

// * function
double multiply (double a, double b)
{
    double c;
    return c = a * b;
}
// / function
double divide (double a, double b)
{
    double c;
    return c = a / b;
}
#endif